public class Counts
{
    public static void main(String[] args)
    {

        for (int num = 0; num <= 30; num++)
        {

            System.out.println("num= " + num);

        }


        for (int num = 30; num >= 0; num--)
        {

            System.out.println("num= " + num);

        }

        for (int num = 0; num <= 18; num +=3)
        {
            System.out.println("num = " + num);
        }

        for (int num = 10; num >= 0; num -=2)
        {
            System.out.println("num = " + num);
        }

    }
}